ESX = exports['es_extended']:getSharedObject()

RegisterNetEvent('GS_FruitFarmer:uv')
AddEventHandler('GS_FruitFarmer:uv', function (vocka)
    local igrac = ESX.GetPlayerFromId(source)
    if igrac then
        igrac.addInventoryItem(vocka, Config.ItemCount)
    end
end)

ESX.RegisterServerCallback('GS_FruitFarmer:proveriItem', function (source, cb, proveriOvo)
    local igrac = ESX.GetPlayerFromId(source)

    if igrac.getInventoryItem(proveriOvo).count >= 1 then
        cb(true)
    else
        cb(false)
    end
end)

RegisterNetEvent('GS_FruitFarmer:pv')
AddEventHandler('GS_FruitFarmer:pv', function (GS5,GS6)
    local GS4 = GS6
    local GS = ESX.GetPlayerFromId(source)
    if GS then
        local GS2 = GS.getInventoryItem(GS5).count
        if GS2 >= 1 then
            GS3 = GS2 * GS4
            GS.removeInventoryItem(GS5, GS2)
            GS.addMoney(GS3)
        end
    else

    end
end)